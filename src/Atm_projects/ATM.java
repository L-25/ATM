package Atm_projects;

public class ATM {
    private double balance;
    private double deposiAmount;
    private double widthAmount;

    public ATM() {
    }

    public double getBalance() {
        return balance;
    }

    public void setBalance(double balance) {
        this.balance = balance;
    }

    public double getDeposiAmount() {
        return deposiAmount;
    }

    public void setDeposiAmount(double deposiAmount) {
        this.deposiAmount = deposiAmount;
    }

    public double getWidthAmount() {
        return widthAmount;
    }

    public void setWidthAmount(double widthAmount) {
        this.widthAmount = widthAmount;
    }
}
